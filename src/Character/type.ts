/// Represents the different types of characters in the game.
///
/// # Description
///
/// An enum where each character type corresponds to a number.
///
/// # Values
///
/// * `TRIANGLE` - Represents a triangle character type (0)
/// * `CIRCLE` - Represents a circle character type (1)
/// * `DIAMOND` - Represents a diamond character type (2)
/// * `FLAG` - Represents a flag character type (3)
/// * `PENTAGON` - Represents a pentagon character type (4)
/// * `STAR` - Represents a pentagon character type (5)

export enum CharacterType {
  TRIANGLE = 0,
  CIRCLE = 1,
  DIAMOND = 2,
  FLAG = 3,
  PENTAGON = 4,
  STAR = 5,
}
